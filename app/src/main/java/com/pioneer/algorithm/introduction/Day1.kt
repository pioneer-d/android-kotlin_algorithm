/** 두 수의 합 구하기 */
fun iSolution1(num1: Int, num2: Int): Int {
    return num1+num2
}

/** 두 수의 차 구하기 */
fun iSolution2(num1: Int, num2: Int): Int {
    return num1-num2
}

/** 두 수의 곱 구하기 */
fun iSolution3(num1: Int, num2: Int): Int {
    return num1*num2
}

/** 몫 구하기 */
fun iSolution4(num1: Int, num2: Int): Int {
    return num1/num2
}
